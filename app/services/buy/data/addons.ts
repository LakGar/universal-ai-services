// Get all unique filters from add-ons
export const getAllFilters = (): string[] => {
  const filterSet = new Set<string>();
  addOns.forEach((addOn) => {
    addOn.filters.forEach((filter) => filterSet.add(filter));
  });
  return Array.from(filterSet).sort();
};

// Get all unique categories
export const getAddOnCategories = (): string[] => {
  const catSet = new Set<string>();
  addOns.forEach((addOn) => catSet.add(addOn.category));
  return Array.from(catSet).sort();
};

